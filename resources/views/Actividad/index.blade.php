<h3>Descenso en Balsa</h3>
Los primero aventureros llegaron al río filobobos en 1988. Desde entonces esta actividad fue bien 
recibida por los visitantes. Nuestro río se divide en tres secciones de aproximadamente 10 km cada
 una para la práctica de rafting y son: alto filo, puente filo y cascada el encanto. Los ríos, dependiendo 
 su peligroso se clasifican del 1 al 5. La categoría 1 corresponde a ríos mansos cuya superficie es 
 semejante a un espejo. Y la categoría 5 son aguas bravas imposibles de navegar. La clasificación del
  Río filobobos es dos y tres, esta es la adecuada para la práctica de rafting sin ningún riesgo. Y al 
  utilizar el servicio de una compañía registrada de descenso estarás completamente seguro para disfrutar 
  de esta actividad; que no requiere de experiencia previa, y es adecuada para toda la familia, sin límites 
  de edades. Los únicos requisitos son disposición al ejercicio y no temer al agua.
