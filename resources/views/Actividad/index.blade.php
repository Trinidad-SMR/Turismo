<h3>Temazcal</h3>
El temazcal fue un ritual practicado por las culturas de nuestros antepasados, donde los participantes recibían un baño de vapor a base de hierbas medicinales y aromáticas. El ritual representa un renacer a la vida.Es un momento donde los aventureros aprovechan para relajarse,  tener un momento de reflexión y meditación. Encontrando una conexión con la madre tierra, una ceremonia de agradecimiento. 

El temazcal es una actividad extra, se necesitan un minimo de 8 personas para poder realizarlo. 
